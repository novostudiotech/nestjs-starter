import type { OpenAPIV3_1 } from 'openapi-types';
import { expect, test } from './fixtures';
import { dereferenceSpec } from './helpers/schema-validator';

type PathItemObject = OpenAPIV3_1.PathItemObject;
type OperationObject = OpenAPIV3_1.OperationObject;

const HTTP_METHODS = ['get', 'post', 'put', 'patch', 'delete'] as const;

function forEachOperation(
  paths: Record<string, PathItemObject>,
  callback: (path: string, method: string, operation: OperationObject) => void
): void {
  for (const [path, pathItem] of Object.entries(paths)) {
    for (const method of HTTP_METHODS) {
      const operation = pathItem[method] as OperationObject | undefined;
      if (operation) callback(path, method, operation);
    }
  }
}

/**
 * Routes generated by Better Auth, admin CRUD framework, or Prometheus
 * that don't have manual @ApiOperation annotations.
 */
function isAutoGenerated(path: string, tags: string[]): boolean {
  return (
    tags.some((t) => t.toLowerCase() === 'auth') ||
    path.startsWith('/admin/') ||
    path.startsWith('/auth/') ||
    path === '/metrics'
  );
}

const EXPECTED_TAGS = [
  'App',
  'Events',
  'Geo',
  'Health',
  'Media',
  'Moderation',
  'Organizers',
  'Organizer Events',
  'User Profile',
  'Event Attendance',
  'Event Suggestions',
];

